#------------------------- Python List Intro------------------------------ 
#rules for  making list 
#int
numbers = [1,2,3,4,5]
print(numbers) 
#output :  [ 1,2,3,4]

#string
names = ['Python','Php','javaScript']
print(names)
#output : ['Python', 'Php', 'javaScript'] 

#mix all datatype
mixed = [1,2,'Python','javaScript',2.3,None]
print(mixed)
#output : [1, 2, 'Python', 'javaScript', 2.3, None]

#access Element
mixed = [1,2,'Python','javaScript']
print(mixed[1])
#output: 2